package codeGym_lvl_4;

//	Display the numbers from 10 to 1 using a while loop. Each value should be on a new line.

public class Assignment_33 {

	public static void main(String[] args) {
		
		int num = 10;
		
		while( num >= 1) {
			System.out.println(num + " ");
			num--;
		}
	}

}
