package codeGym_lvl_4;

//		Display the numbers from 1 to 10 using a while loop. Each value should be on a new line.

public class Assignment_32 {

	public static void main(String[] args) {
		
		int num = 1;
		
		while (num <= 10) {
			System.out.println(num + " ");
			num++;
		}

	}

}
